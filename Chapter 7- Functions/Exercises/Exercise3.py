## Exercise 3: T-Shirt

# Writing the message function
def make_shirt(size, msg):
    print (f"\nI am going to tailor an {size} t-shirt.")
    print (f'The t-shirt will be printed with, "{msg}"')

# Calling the function of the shirts' sizes and prints
make_shirt (msg = "Illecebra and Arcanus.", size = 'XS')
make_shirt ('XL', 'Midas Touch.')

print ("\n")