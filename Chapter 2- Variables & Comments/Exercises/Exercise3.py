## Exercise 3: Stripping Names

name = "\tLaurent Borcelle\n"

print ("Unmodified:")
print (name)

# lstrip() function
print ("\nUsing lstrip():")
print (name.lstrip())

# rstrip() function
print ("\nUsing rstrip():")
print (name.rstrip())

# strip() function
print ("\nUsing strip():")
print (name.strip())

# Additional line break
print ("\n")